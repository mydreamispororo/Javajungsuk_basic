package ch03;

public class IncreasingOpt_Ex {

	public static void main(String[] args) {
		//증감 연산자 ++, --
		//++i 지금 당장 / i++ 한줄 내려서 증감
		int i, j;
		i = 5;
		j = 5;
		System.out.println(i);
		
	
		//부호변경
		int k = - 10;
		k = - k;
		System.out.println(k);

		//-10 * -1 = 10
		//abs(-10) = 10
		
		
		
		
	}

}
