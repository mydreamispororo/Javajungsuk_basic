package ch02;

public class Print_Ex02 {

	public static void main(String[] args) {
		//print "숫자" -> 글자
		//"글자" + 숫자 = 글자
		//숫자 + "글자" = 글자
		//숫자 + 숫자 = 숫자
		//연산자 우선 : () * / + -
		System.out.println("3 + 5 = " + (3 + 5));
		System.out.println("3" + 0);
		char c = 'A'; //char 타입은 문자를 표현하지만, 숫자 연산 가능
		System.out.println((int) c);
		System.out.println(c + 10);

		
		
		
	}

}
