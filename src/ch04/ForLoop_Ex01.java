package ch04;

public class ForLoop_Ex01 {

	public static void main(String[] args) {
		//for 가로 세로
		for(int i = 0; i < 10; i++) {
			System.out.print(i);
		}
		
		for(int i = 0; i < 10; i++) {
			System.out.println(i);
		}
		
		
		//for 예제
		
		for(int i = 0; i < 9; ++i) {
			
			System.out.print(" | " + (i + 1));
			if(i == 8) {
				System.out.print(" | ");
			}
		
		

	}

	}
}
