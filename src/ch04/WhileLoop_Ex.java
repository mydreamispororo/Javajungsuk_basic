package ch04;

public class WhileLoop_Ex {

	public static void main(String[] args) {
		for(int i = 0; i < 10; i++) {
			System.out.println(i);
		}		
		
		//while반복
		int i = 0;
		while(i < 10) {
			System.out.println(i);
			i++;
		}
		
		//무한루프
		boolean isRun = true;
		while(isRun) {}
			
			
		boolean isRun1 = false;
		while(!isRun) {}
		
		while(true) {} //scanner 만나면 멈춤
			
		
		
		
		
		
		
		
		
	}

}
